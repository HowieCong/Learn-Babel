// What is Babel?
// Babel 是什么？

// Babel is a JavaScript compiler that mainly converts newer JavaScript (like ES6+) code into backward-compatible JavaScript, so it can run in older or unsupported browsers/environments.
// Babel 是一个 JavaScript 编译器，主要用于将新版本的 JavaScript（如 ES6+）代码转换为向后兼容的 JavaScript，以便可以在老旧或不支持新特性的浏览器/环境中运行。

// Simple example: Convert ES6 arrow function to ES5 regular function
// 简单例子：将 ES6 箭头函数转为 ES5 普通函数

const add = (a, b) => a + b;
console.log(add(2, 3)); // Output: 5 // 输出： 5

